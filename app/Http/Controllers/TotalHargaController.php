     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, FoodOrder $food, DrinkOrder $drink)
    {
        $total_harga = new TotalHarga();
        $total_harga->id = $request->id;
        $total_harga->makanan_dipesan = $food->makanan_dipesan;
        $total_harga->minuman_dipesan = $drink->minuman_dipesan;

        $total_harga->save();

        return redirect('/home')->with('toast_success', 'Menu berhasil ditambahkan!');
    }

